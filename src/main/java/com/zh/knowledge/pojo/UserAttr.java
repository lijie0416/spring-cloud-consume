package com.zh.knowledge.pojo;

public class UserAttr {

	private long id;
	private long CUserId;
	private String userIphone;
	private String userMechanism;
	private String userProvince;
	private String userMailbox;
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public long getCUserId() {
		return CUserId;
	}
	public void setCUserId(long cUserId) {
		CUserId = cUserId;
	}
	public String getUserIphone() {
		return userIphone;
	}
	public void setUserIphone(String userIphone) {
		this.userIphone = userIphone;
	}
	public String getUserMechanism() {
		return userMechanism;
	}
	public void setUserMechanism(String userMechanism) {
		this.userMechanism = userMechanism;
	}
	public String getUserProvince() {
		return userProvince;
	}
	public void setUserProvince(String userProvince) {
		this.userProvince = userProvince;
	}
	public String getUserMailbox() {
		return userMailbox;
	}
	public void setUserMailbox(String userMailbox) {
		this.userMailbox = userMailbox;
	}
	
	
	
	
}
